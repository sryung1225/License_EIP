#include <stdio.h>
int main() {
    /* 연산자 우선순위
       1. 단항   ( ! ~ ++ -- )
       2. 산술   ( * / % )
       3. 산술   ( + - )
       4. 시프트 ( << >> )
       5. 관계   ( < <= >= > )
       6. 관계   ( == != )
       7. 비트   ( & ^ | )
       8. 논리   ( && || )
       9. 조건   (조건?수식1:수식2)
       10. 대입  (= += -= *= /= %= <<= >>=)
    */ 

    int a,b,c,d;
    a=3; b=4; c=5; d=6;

    int i = a*b+c>=d&&d/a-b!=0;
    // ((a*b)+c)>=d && (d/a)-(b!=0)
    // ((3*4)+5)>=d && (6/3)-(4!=0)
    // (12+5)>=d && (2-1(참))
    // 17>=6 && 1
    // 1(참) && 1
    // 1
    printf("%d ", i); // 1

    int j = d % b + ++a * c-- || c - --a >= 10;
    // d%b+(++a)*(c--) || c-(--a)>=10
    // (6%4)+(3*5) || (5-3)>=10
    // 2+15 || 2>=10
    // 17 || 0(거짓)
    // 참(17) || 참(4)
    // 참(1)
    printf("%d ", j);// 1
}