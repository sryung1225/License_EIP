#include <stdio.h>
int main() {
	int a = 2, b = 3, c = 4, d, e;
	d = a & b & ~b;
	e = a | b & c;
	printf("%d %d", d, e);
}

// a = 2 = 2진수(0010)
// b = 3 = 2진수(0011)
// c = 4 = 2진수(0100)

// ~b = 1111 1101
// d = 0000 0010 & 0000 0011 & 1111 1101 = 0000 0010 & 1111 1101 = 0000 0000 = 0
// e = 0010 | 0011 & 0100 = 0010 | 0000 = 0010 = 2

// 결과
// 0 2