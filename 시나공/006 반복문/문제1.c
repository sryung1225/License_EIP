#include <stdio.h>
int main() {
    int n=0, t=0;
    do {
        t += n;
        printf("%d, %d\n", n++, t);
    } while (n<10);
}

// n=0 t=0 printf("0, 0") n=1
// n=1 t=0+1=1 printf("1, 1") n=2
// n=2 t=1+2=3 printf("2, 3") n=3
// n=9 t=1+2+3+...+9=45 printf("9, 45") n=10 반복문 탈출

// 최종적으로 출력되는 결과 : 9, 45
