// 2차원 배열

#include <stdio.h>
int main()
{
	int a[3][4];
	int i, j, k = 0;
	for (i = 0; i < 3; i++)
	{
		for (j = 0; j < 4; j++)
		{
			k++;
			a[i][j] = k;
		}
	}
}

// i=0 j=0 a[0][0]=1
// i=0 j=1 a[0][1]=2
// ...
// i=2 j=3 a[2][3]=12

